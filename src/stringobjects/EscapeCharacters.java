package stringobjects;

public class EscapeCharacters {
    public static void main(String[] args) {
        /*
         * \n -- new line
         * \" -- double quotes
         * \t -- tab
         * \' -- apostrophe
         * \\ -- back slash
         */

        System.out.println("This is Alex\'s Computer");
        System.out.println("This is \"Alex\'s\" Computer");
        System.out.println("This is in \n new line");
        System.out.println("Name\tAge\tdepartment");
        System.out.println("This is a backslash \\");
    }
}
